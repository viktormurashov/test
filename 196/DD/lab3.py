# АЛГОРИТМ: реализован КОРРЕКТНО, но НЕЭФФЕКТИВНО
# Логика правильная: для каждого элемента ищем первый меньший справа
# Сохраняется индекс элемента (по примеру это правильно)
# ============================================================================
# КРИТИЧЕСКАЯ ПРОБЛЕМА ПРОИЗВОДИТЕЛЬНОСТИ: O(n^2)
# Для каждого элемента проходим все элементы справа
# Можно оптимизировать до O(n) используя стек (алгоритм "Next Smaller Element")
# ============================================================================
# КРИТИЧЕСКАЯ ОШИБКА: лишние отладочные print-ы (строки 33-34)
# По заданию формат вывода: только числа через пробел
# ============================================================================
# ПРОБЛЕМА: неиспользуемая переменная `found` (строка 26)
# ============================================================================
# ПРИЗНАКИ LLM: неоптимальный алгоритм, отладочные print-ы, неиспользуемые переменные
# ============================================================================

n = 5

arr = [2, 3, 1, 5, 4]

result = [-1] * n

for i in range(n):
    found = False
    for j in range(i + 1, n):
        if arr[j] < arr[i]:
            result[i] = j
            found = True
            break

print(f"Кол-во элементов в массиве: '{n}'")
print(f"Массив: '{arr}'")
print(' '.join(map(str, result)))