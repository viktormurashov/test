# АЛГОРИТМ: реализован КОРРЕКТНО
# Алгоритм наивного поиска подстроки O(n*m), для задачи приемлем
# ============================================================================
# КРИТИЧЕСКАЯ ОШИБКА: лишние отладочные print-ы (строки 9-10)
# По заданию формат вывода: только индексы через пробел
# Текущий код выводит дополнительную информацию, что нарушает формат
# ============================================================================
# ПРИЗНАКИ ИСПОЛЬЗОВАНИЯ LLM:
# - Отладочные print-ы с форматированием (f-strings с описаниями)
# - Смешение логики и отладки в одном коде
# ============================================================================

S = input().strip()
T = input().strip()

result = []
for i in range(len(S) - len(T) + 1):
    if S[i:i+len(T)] == T:
        result.append(i)

print(f"Строка S: '{S}'")
print(f"Строка T: '{T}'")
print(f"Результат: {' '.join(map(str, result))}")

# ============================================================================
# РЕВЬЮ КОДА: ЗАДАЧА 2.2 - Циклический сдвиг
# ============================================================================
# КРИТИЧЕСКАЯ ОШИБКА #1: неправильная логика проверки сдвига!
# Строка 20: `S == T[shift:] + T[:shift]` проверяет, является ли S
# циклическим сдвигом T ВЛЕВО на shift позиций
# По заданию: нужно проверить, является ли T циклическим сдвигом S ВПРАВО
# Правильно: `T == S[-shift:] + S[:-shift]` для сдвига вправо
# Или: `T == (S + S)[shift:shift+len(S)]` для сдвига вправо
# ============================================================================
# КРИТИЧЕСКАЯ ОШИБКА #2: неправильный порядок проверки
# Код сначала проверяет длины, потом ищет сдвиг
# Но если длины разные, нужно сразу вернуть -1 (это правильно)
# Однако логика сдвига все равно неверна
# ============================================================================
# КРИТИЧЕСКАЯ ОШИБКА #3: лишние отладочные print-ы
# По заданию формат вывода: только число (сдвиг или -1)
# ============================================================================
# ПРИЗНАКИ LLM: отладочные print-ы, неправильное понимание направления сдвига
# ============================================================================

if len(S) != len(T):
    print(f"Строка S: {S}")
    print(f"Строка T: {T}")
    print("Результат: -1")
else:
    found = -1
    for shift in range(len(S)):
        if S == T[shift:] + T[:shift]:
            found = shift
            break
    
    print(f"Строка S: {S}")
    print(f"Строка T: {T}")
    print(f"Результат: {found}")