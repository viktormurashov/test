s, t = input(), input()
 
if len(s) != len(t):              # Проверка 1: если строки разной длины, они не могут быть циклическими сдвигами
    print(-1)                     # выводим -1 как признак невозможности

elif s == t:                      # Проверка 2: если строки уже равны, сдвиг не требуется
    print(0)                      # выводим 0 - нулевой сдвиг

else:                             # Основной случай: строки одинаковой длины, но разные
    cycle = s + s                 # Создаем удвоенную строку s для поиска циклических сдвигов


    # Компактная проверка с тернарным оператором:
    # - Сначала проверяем, содержится ли t в cycle (t in cycle)
    # - Если ДА: вычисляем размер сдвига как len(s) - cycle.find(t)
    # - Если НЕТ: выводим -1
    print(len(s) - cycle.find(t) if t in cycle else -1)